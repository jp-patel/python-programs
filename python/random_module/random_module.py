import random

# how to print a random number
print(random.randrange(1, 10))
print()

# how to print a random character form string
my_string="hello"

print(random.choice(my_string))
print()

# how to print a random item from list
my_list=["apple","banana","cherry"]

print(random.choice(my_list))
print()

# how to shuffle list items
print(random.shuffle(my_list))
print()
